    <h3><strong>Recommended amount</strong></h3>
    <p>UK Visa and Immigration requires you to have a budget of £1,020 per month to study in London. This amount is in addition to the tuition fees. This is the minimum amount you need to show to get a visa to study in London. For other places in UK, the amount will be lesser. If you study at colleges outside London, you will require a minimum amount of £820 per month as requested by UK Visa and Immigration. Average accommodation rents are £150 per week. The UKVI also estimates that in addition to your rent, you will need approximately £104 every week for living costs. This amount will differ according to your lifestyle and will depend on how much you socialize.</p>		<!-- Download Guide Widget STARTS-->


