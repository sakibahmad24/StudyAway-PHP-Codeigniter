    <p>For international students, undergraduate tuition fees is around £8,000 - £30,000 per year for full-time courses. Note that the higher range is for medical degrees at the best colleges. However, on average, international undergraduate fees is around £12,000 per year.</p>
    <p>For postgraduate degrees, the average tuition fees for international students for full-time programs range from £10,000 - £13,000 per year. For laboratory and research-based programs, average annual fees stand at £14,000. For medical degrees, the average figure is £21,000.</p>
    <p>As far as living costs are concerned, these costs will vary greatly depending on your lifestyle, spending habits and budget. These costs are given by the month. You can calculate your estimated costs based on how long you will be in <a href="https://studyabroad.shiksha.com/uk" target="_blank">UK</a>.</p>
