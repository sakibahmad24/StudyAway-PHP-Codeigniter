    p>Getting a post work permit in Canada (called PGWP) depends a lot on your course and its duration. A work permit under the&nbsp;PGWP&nbsp;is usually issued for the length of the study program. The maximum length of the post work permit is of three years. It means if your post-graduate course&rsquo;s duration is two years, you get a three year PGWP visa.</p>
<p>A post-graduation work permit cannot be valid for longer than the student&rsquo;s study program. The course duration must be a minimum of eight months in length.&nbsp;</p>
<p>The&nbsp;PGWP&nbsp;allows international students graduating from a participating post-secondary college/university to gain Canadian work experience. In the long run, Canadian work experience gained through&nbsp;PGWP&nbsp;helps international graduates qualify for PR (permanent residence) in Canada through&nbsp;<a href="https://studyabroad.shiksha.com/express-entry-canada-articlepage-1269" target="_blank">Express Entry</a>.</p>    <br/>
<h2>Eligibility criteria for PFWP</h2>
<ul>
<li>You must have studied&nbsp;full time&nbsp;in Canada and your course duration should be at least eight months.</li>
<li>You must apply for a work permit within 90&nbsp;days of completing your course. You must have written proof from the college/university indicating the same. For example, a transcript or an official letter.</li>
<li>You must have a valid student visa when you apply for the PGWP.</li>
</ul>
<h2>Duration</h2>
<p>If the official length of your program of study is:</p>
<ul>
<li>Less than&nbsp;eight months: you are not eligible for the PGWP program</li>
<li>Less than&nbsp;two years&nbsp;but more than eight months: you will get a work permit for a period equal to your course duration</li>
<li>Two years or more: you will be granted a work permit three years</li>
</ul>


