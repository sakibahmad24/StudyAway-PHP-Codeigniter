        <p>By working while studying in Canada, you get a great job experience especially if you have plans of applying for PR (permanent residence) ultimately.</p>
        <p>On the Canadian student visa, you have the permission of working for <strong>20 hours/week</strong> of a paid job. However this is the maximum number of hours, and you are free to work for lesser number of hours. Usually those students enrolled for difficult or intensive courses are recommended by the universities to work for a maximum of 12 hours a week. This is because working for more than this number can affect a student's performance in studies. So you can reduce the number of the working hours if you feel you are unable to maintain a work-study-life balance.</p>
        <h2><strong>Monetary Pay</strong></h2>
        <p>Usually, for part-time jobs, students are paid by hours. The rate of pay is around <strong>$10 an hour</strong>, on average. If your aim is only to gain work experience, like helping your professor with research work, then you donâ€™t need a work permit. This kind of work has to be on-campus and will be paid less; you can work beyond the stipulated number of hours. So with just the student visa, you can still engage in academic work on-campus.</p>
        <p>If an international student wishes to work off-campus, he can do apply for an off-campus work permit after completing six months of study. That permit will allow the student to work for a maximum of 20 hours/week off-campus.</p>


