    <p>Technology and health care were the two industries with the most job openings, plus the highest pays. As an international student, it is important to know what the opportunities are in the chosen sector of studies. Looking at the data from the survey it is clear, Medicine and Computer Science are the most popular jobs nationwide.</p>
<p>Being a Physician/doctor is the most prestigious and well-paying job in US with the base salary starting from $212,000 per year. However, medical studies are really tough and passing USMLE is mandatory to get the ‘license to practice’ in the US. At number two most popular job is Pharmacy Manager, but it has a comparatively smaller number of job openings per year. The third most popular among the high paying job is that of the Software Architect. So clearly those students looking for careers in Medicine and Computer Sciences benefit the most in the US after they finish their courses. </p>
