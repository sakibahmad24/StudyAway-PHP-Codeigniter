    <p>These figures, however, talk about the basics and would vary from university to university as well as the style of living you wish to maintain. If you would rather wish to stay in an independent accommodation or alternatively, your university does not have a hostel facility (though rare), then you would end up spending anywhere between AUD 1000 – AUD 1750 for a single room unit. Also, the level of expense may vary considerably for entertainment. If you are an exploring kind and would rather spend your weekend exploring the magnificent outback, the monthly expenses can rough denote to your weekly expenses as well.</p>
    <p>So in all, it depends on how you wish to live. There’s a lot to spend on in Australia…but you need not despair. You can always earn that extra amount to make your stay worthwhile.</p>
    <p><strong>Working while studying</strong></p>
    <p>Australian Immigration Laws are comparatively easy when it comes to working while studying. A slight request would often make you eligible to work in Australia as an international student. Though the rules might vary, generally students are allowed to work for up to 40 weeks in a year.</p>
    <p>The other aspect if the availability of job and there is no dearth of the same either. Companies, local businesses, and retail shops often hire students (both domestic and international). As an international student, you are protected by the same labor laws as the domestic student populace which also means the minimum wages. As a student, you can expect around $17 per hour.</p>

    <p><em>Note: These amounts will differ from place to place within Australia</em></p>

