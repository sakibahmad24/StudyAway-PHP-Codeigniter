    <p>As an F1 student visa holder, international students are able to complete up to a year of temporary employment directly related to the major field of study.</p>
<p>Optional Practical Training (OPT) is a one-year work and stay-back permit given to international students&nbsp;on completion of a degree program in the US. There are two types of OPT &ndash; one which can be used during one&rsquo;s studies i.e. PreCompletion OPT, and another which starts after completing one&rsquo;s studies i.e. Post-Completion OPT.</p>
<p>The job you get after finishing your course must be&nbsp;directly related to your field of study. You have to find the job within 90 days after graduation.</p>  <br/>
<p><strong>Duration of allowed stay &ndash;</strong> You get to stay in the US for one year after completion of your course on an F1 visa.</p>
<p><strong>Extension &ndash;</strong> For students studying STEM courses (Science, Technology, Engineering, and Mathematics) a new rule allows a 24-month extension. Which means you can continue to stay in the US for three years after finishing your course of study.</p>
<h3><strong>How to get OPT permit</strong></h3>
<p>The first thing is to have the Designated School Official (DSO) at your university recommend your case for OPT. A DSO is someone who assists and manages the status of international students. To be put forward, you will need to complete a certification process at your university. This includes submitting OPT I-20 request form to International Services Office. You can download this form from your university&rsquo;s website.</p>
<p>Your university will then send your request to SEVIS (Student and Exchange Visitor Information System) and on successful completion, a new I-20 immigration form will be generated.</p></div>



