    <p>UK has always been an attractive study destination for Indian students, but staying in UK after you finish your course can be difficult. Four months stay back is available after course completion. In some cases, students applying to top universities can also get 6 months of stay back to find a job.&nbsp;</p>
<p>Presently, you need to apply for Tier 2 visa (work visa) immediately on finding a job after finishing studies. Your Tier 4 visa is valid only till your course duration.</p> <br/>
<p>Within four months of completing the course, international students must find a job with a minimum salary of &pound;20,800 with a Tier 2 sponsor license. Also, students pursuing Ph.D. are allowed to stay up to 12 months after completing their degree.</p>
<p>In statements issued by the UK govt., they have stressed that "the brightest and best of students should not come here and do menial jobs". There have been many petitions started by UK universities, Scottish govt., and Indian diplomats to convince the UK govt. to reinstate Tier 1 post-study work visa. However, till now the UK has ruled out re-introducing post-study work visa.</p></div>

    
