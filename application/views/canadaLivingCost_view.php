    <p>Studying in Canada is considered a rewarding investment for your future as you have access to premier education, cross-cultural experiences, and a globally recognized degree. For international students, getting admitted to universities and colleges in Canada requires planning and financial commitment before you are there.</p>

    <p>International students will have to meet the costs of studies and living in Canada. Citizenship and Immigration Canada (CIC) requires some proof that you has sufficient money and funds available. Your college provides cost of living estimate, the immigration officer will follow it. If your college doesnâ€™t provide it, you will be required to show evidence of $10,000 for 12 months living expenses for a student. Additional $4,000 will be required for spouse and another $3,000 for each dependent. This amount includes accommodation, food, medical insurance, transportation, books, and clothing. Tuition and other fees are not included in this amount.</p>

    <p>It is vital that students obtain medical and hospital insurance as healthcare in Canada is very expensive. Public health insurance plans are administered independently in each province. Some universities require compulsory insurance coverage. The coverage should be secured within the first week after arrival. </p>

    <h6>Big city life vs. Suburban areas</h6>
    <p>Like anywhere in the world, living costs in urban centers are more than those in the suburbs. Many students prefer on-campus housing much more, which may not be the lowest-cost option always, but is definitely more convenient. Toronto and Vancouver are the most expensive cities to live at and with the shortage of affordable housing in Toronto, costs are the highest there and still continue to rise.</p>

    <h6>Based on educational institutions</h6>
    <p>As the cost of living varies from city to city and depending on where the school is located, the best estimated living expense will depend on the university you are applying to. As mentioned earlier, every college abroad provides estimated fee and living expenses. Some colleges have reasonable and affordable on-campus boarding options, some colleges charge quite a lot for the same.</p>

