    <p>Australia is a famous study abroad destinations for Indian Students, and also for International Students from across the world. While an internationally acclaimed quality education system is one of the many attractions, the fact remains that students wish to explore the country’s many tourist haunts as well. Also, though the education might be cheaper in comparison to USA (top choice for students planning to study abroad), the <a href="https://studyabroad.shiksha.com/international-student-life-in-australia-cost-of-living-work-entertainment-options-articlepage-378" target="_blank">living expenses in Australia</a> are fairly high. As such, students studying in Australia can gain a good amount of pocket money to both supplement their living costs as well as enjoying the stay in the country. And the country offers more than just permission to work while studying in Australia.</p>
    <p> </p>
    <p><strong>Work Permissions on Student Visa</strong></p>
    <p>Student Visas for Australia have changed with effect July 1, 2016. The earlier Subclasses 571 - 576 have now been grouped under SubClass 500. As per the new rules, students going to Australia under Visa Subclass 500 are eligible to work for up to 40 hours in a fortnight while the course is in Session and for unlimited hours when the course is not in session (during breaks). The only proviso is that the student cannot start working till the course commences.</p>
    <p>Also, a fortnight refers to a period of 14 days starting on a Monday and ending on the Sunday of the following week. The hours can be divided as per student’s convenience. For instance, a student may work for 10 hours in week one and 30 hours in week 2 or 15 and 25 hours respectively. Students must, however, note that in the above example, if the student works for 30 hours in week 2, then he/she can only work for 10 hours in the following week or else the condition of 40 hours in a fortnight would be breached – which in turn can lead to cancellation of Visas. Ideally, a student looking for continuous part-time work must limit their working to about 20 hours a week.</p>
    <p>A few things to remember:</p>
    <ul>
        <li>Students cannot work until the session begins. For instance, say your course starts January 2019 and you reach Australia by December 2018, then you cannot take up employment till your session begins.</li>
        <li>In case work is a normal part of the curriculum of the course you are enrolled for then this limit does not apply. This is usually applicable in case of compulsory internships of integrated courses.</li>
        <li>Also, students that fall under the Post Graduate Research Sector can work for unlimited hours when the research work or doctoral thesis begins. Also, in case there are any preliminary courses that the student has enrolled into, the student is eligible to work as per the 40 hours per fortnight rule mentioned above.</li>
        <li>Students completing the Australian study requirement can apply for a Post Study Work Permit under Graduate Stream. However, students enrolled in an ELICOS cannot apply for a Post Study Work Permit for Australia.</li>
    </ul>
    <p>* Note that if students applying under the category Post Graduate Research Sector are taking a preliminary course, they are eligible to work only for 40 hours in a fortnight during the tenure of the course he/she is enrolled in. Only when the Research Course begins does the student have permission to work for unlimited hours. </p>
    <p>Do remember, you would need to apply for and get a Tax File Number to work part-time. As an international student with a foreign passport, you can <a href="https://www.ato.gov.au/" target="_blank" rel="nofollow">apply for a TFN online from their official website</a>.</p>
    <p>What laws apply to International Student for working while studying?</p>
    <p>The Australian government allows all the students the same privilege when it relates to working while studying. As such you have the right to</p>
    <ul>
        <li>A minimum wage as stipulated under the <a href="https://www.fairwork.gov.au/" target="_blank" rel="nofollow">Fair Work</a> Act 2009</li>
        <li>Challenge of unfair dismissal from the job</li>
        <li>Breaks and rest periods</li>
        <li>A healthy and safe work environment</li>
    </ul>
    <p> </p>
    <p><strong>Types of Part-Time Work Options Available</strong></p>
    <p>In case you are a professional who is taking up further education from Australia, you can apply for and get a job commensurate with your qualification. Apart from that, usually students get part-time jobs in the following sectors</p>
    <ul>
        <li>Retail sales jobs in supermarkets, departmental stores</li>
        <li>In restaurants, cafes, and bars</li>
        <li>In hotels and motels (both in the kitchens and administration and housekeeping)</li>
        <li>Farming and related activities</li>
        <li>Sales and telemarketing</li>
        <li>Administration or Clerical roles in different companies</li>
        <li>As a Tutor – both personal as well as in institutions</li>
    </ul>
    <p>To conclude, Australia offers excellent part-time working opportunities for international students. While it would not be enough to subset the tuition fees, it is usually able to assist a moderate living style. Also, there are many options available. Hence, if you are planning to Study in Australia, be assured there is more to it than just quality education. All the best!</p>		<!-- Download Guide Widget STARTS-->

